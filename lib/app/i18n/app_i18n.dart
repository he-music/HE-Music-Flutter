  static String t(AppConfigState config, String key) {
    return tByLocaleCode(_resolveLocaleCode(config.localeCode), key);
  }

  static String tByLocaleCode(String localeCode, String key) {
    final languagePack = _localized[localeCode] ?? _localized[_fallbackLocale]!;
    return languagePack[key] ?? key;
  }

  static String format(
    AppConfigState config,
    String key,
    Map<String, String> replacements,
  ) {
    return formatByLocaleCode(
      _resolveLocaleCode(config.localeCode),
      key,
      replacements,
    );
  }

  static String formatByLocaleCode(
    String localeCode,
    String key,
    Map<String, String> replacements,
  ) {
    var value = tByLocaleCode(localeCode, key);
    for (final entry in replacements.entries) {
      value = value.replaceAll('{${entry.key}}', entry.value);
    }
    return value;
  }

  static String _resolveLocaleCode(String localeCode) {
    if (localeCode != 'system') {
      return localeCode;
    }
    final platformCode = PlatformDispatcher.instance.locale.languageCode;
    return _localized.containsKey(platformCode)
        ? platformCode
        : _fallbackLocale;
  }
